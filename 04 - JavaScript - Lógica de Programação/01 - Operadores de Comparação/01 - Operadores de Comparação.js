/**
 * >    Greater than
 * >=   Greater than or equal to
 * <    Less than
 * <=   Less than or equal to
 * ==   Equality (value) ***************
 * ===  Strict equality (value and type)
 * !=   Different (value) ***************
 * !==  Strict different (value and type)
 */

console.log("<>");
console.log(10 > 5);
console.log(10 >= 5);
console.log(5 < 5);
console.log(10 <= 5);

console.log("==");
console.log(10 == 10);
console.log(10 == "10");
console.log(10 === "10");

console.log("!=");
console.log(10 != "10");
console.log(10 !== "10");