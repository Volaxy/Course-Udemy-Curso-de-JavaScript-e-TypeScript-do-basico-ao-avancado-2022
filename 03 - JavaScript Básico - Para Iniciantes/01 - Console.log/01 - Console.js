// The "console.log()" prints something in the console screen.
// The "console.log()" by default breaks a line.
console.log("Hello, 'World!'"); // To insert the ' into the string, put the string between "".
// The ";" is optional in JavaScript.
console.log('Hello, "World!"'); // To insert the " into the string, put the string between ''.
console.log(`'Hello', "World"!`); // The `` is need to use "Template Strings".

// This is a literal number on the console
console.log(123);
console.log(15.75);

console.log(1, 2.5) // To print multiple values, just put the "," to separate the values.